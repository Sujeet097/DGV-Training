package models;

public class Quiz {
	    String name;
	   
	    int points;

	

	public Quiz(String name, int points) {
		
			this.name = name;
			this.points = points;
		}



	public String getName() {
		return name;
	}



	public int getPoints() {
		return points;
	}

	
	
	   
	   

}
